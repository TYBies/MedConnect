// Merge additional translations into main translations
export function mergeTranslations(translations) {
    Object.keys(additionalTranslations).forEach(lang => {
        if (translations[lang]) {
            Object.assign(translations[lang], additionalTranslations[lang]);
        }
    });
    return translations;
}